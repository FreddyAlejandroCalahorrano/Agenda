    private void BtnVerAgendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnVerAgendaActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) tblAgenda.getModel();

        String[] c = new String[11];

        try {
            for (int i = 0; i < 12; i++) {
                _agenda.siguiente();
                c[0] = String.valueOf(_agenda._cliente().getInt("claveContacto"));
                c[1] = _agenda._cliente().getString("nomContacto");
                c[2] = _agenda._cliente().getString("apellContacto");
                c[3] = _agenda._cliente().getString("dirContacto");
                c[4] = _agenda._cliente().getString("tlfContacto");
                c[5] = _agenda._cliente().getString("celContacto");

                modelo.addRow(c);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Agenda.class.getName()).log(Level.SEVERE, null, ex);
        }

        tblAgenda.setModel(modelo);
    }//GEN-LAST:event_BtnVerAgendaActionPerformed

